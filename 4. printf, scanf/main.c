#include <stdio.h>
#include <stdlib.h>

int main() {

    int zahl;

    /*
        %d für int
        %c für chars
        %f für floats
        %lf für doubles
    */
    scanf("%d", &zahl);

    /*
        %d für int
        %c für chars
        %f für floats
        %lf für doubles
    */
    printf("zahl = %d", zahl);

    return 0;
}